BEGIN;

DROP TABLE IF EXISTS "user", "image", "category", "collection", "item", "item_has_image", "cart", "cart_item", "order_detail", "order_user" CASCADE;

CREATE TABLE "user" (
  "id" INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  "nom" VARCHAR(255) NOT NULL,
  "prenom" VARCHAR(255) NOT NULL,
  "email" VARCHAR(255) NOT NULL UNIQUE,
  "password" VARCHAR(255) NOT NULL,
  "role" VARCHAR(255) DEFAULT 'user',
  "is_logged" BOOLEAN DEFAULT FALSE,
  "created_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  "updated_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE "image" (
  "id" INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  "name_image" VARCHAR(255) NOT NULL,
  "url" VARCHAR(255) NOT NULL,
  "created_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  "updated_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE "category" (
  "id" INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  "name_category" VARCHAR(255) NOT NULL,
  "created_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  "updated_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE "collection" (
  "id" INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  "title_collection" VARCHAR(255) NOT NULL,
  "description_collection" TEXT,
  "image_id" INTEGER,
  "is_active" BOOLEAN DEFAULT TRUE,
  "created_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  "updated_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY ("image_id") REFERENCES "image" ("id") ON DELETE CASCADE
);

CREATE TABLE "item" (
  "id" INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  "title" VARCHAR(255) NOT NULL,
  "slug" VARCHAR(255) NOT NULL UNIQUE,
  "description" TEXT,
  "price" DECIMAL(10, 2) NOT NULL,
  "size" VARCHAR(255) NOT NULL,
  "stock" INTEGER NOT NULL,
  "quantity" INTEGER NOT NULL,
  "collection_id" INTEGER,
  "category_id" INTEGER NOT NULL,
  "is_active" BOOLEAN DEFAULT TRUE,
  "created_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  "updated_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY ("collection_id") REFERENCES "collection" ("id") ON DELETE CASCADE,
  FOREIGN KEY ("category_id") REFERENCES "category" ("id") ON DELETE CASCADE
);

-- TABLE DE LIAISON ITEM_IMAGE
CREATE TABLE "item_has_image" (
  "id" INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  "item_id" INTEGER NOT NULL,
  "image_id" INTEGER NOT NULL,
  "created_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  "updated_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UNIQUE ("item_id", "image_id"),
  FOREIGN KEY ("item_id") REFERENCES "item" ("id") ON DELETE CASCADE,
  FOREIGN KEY ("image_id") REFERENCES "image" ("id") ON DELETE CASCADE
);

CREATE TABLE "cart" (
  "id" INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  "user_id" INTEGER REFERENCES "user" ("id") ON DELETE CASCADE,
  "total" DECIMAL(10, 2) NOT NULL,
  "created_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  "updated_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE "cart_item" (
  "id" INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  "cart_id" INTEGER REFERENCES "cart" ("id") ON DELETE CASCADE,
  "item_id" INTEGER REFERENCES "item" ("id") ON DELETE CASCADE,
  "quantity" INTEGER NOT NULL,
  "created_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  "updated_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE "order_user" (
  "id" INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  "user_id" INTEGER NOT NULL,
  "total" DECIMAL(10, 2) NOT NULL,
  "status" VARCHAR(20) DEFAULT 'en attente',
  "created_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  "updated_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY ("user_id") REFERENCES "user" ("id") ON DELETE CASCADE
);

CREATE TABLE "order_detail" (
  "id" INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  "order_id" INTEGER NOT NULL,
  "item_id" INTEGER NOT NULL,
  "item_name" VARCHAR(255) NOT NULL,
  "quantity" INTEGER NOT NULL,
  "price" DECIMAL(10, 2) NOT NULL,
  "created_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  "updated_at" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY ("order_id") REFERENCES "order_user" ("id") ON DELETE CASCADE,
  FOREIGN KEY ("item_id") REFERENCES "item" ("id") ON DELETE CASCADE
);

COMMIT;
